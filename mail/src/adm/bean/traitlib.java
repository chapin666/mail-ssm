package adm.bean;

public class traitlib {

	private int id;
	private String hostaddress;
	private String date;
	private int start;
	private int end;
	
	
	
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getEnd() {
		return end;
	}
	public void setEnd(int end) {
		this.end = end;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getHostaddress() {
		return hostaddress;
	}
	public void setHostaddress(String hostaddress) {
		this.hostaddress = hostaddress;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
}
